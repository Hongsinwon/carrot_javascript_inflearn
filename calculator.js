// 계산기란 무엇인가? => + - *  %
// 1 + 1 = 2
// 2 * 3 = 6
// 4 - 10 = -6
// 4 / 2  = 2

function 더하기(a, b) {
  console.log(a + b); //console.log <- 함수
}

더하기(10, 5); // 15

function 빼기(a, b) {
  console.log(a - b);
}

빼기(100, 50); //50

function 곱하기(a, b) {
  console.log(a * b);
}

곱하기(9,9); //81

function 나누기(a, b) {
  console.log(a / b);
}

나누기(10, 5) //2