let 택배들 = [];

택배들.push("아이패드");
택배들; // (1)["아이패드"]
택배들.push("햇반");
택배들; // (2)["아이패드","햇반"]
택배들.push("생수");
택배들.push("피아노");
택배들; // (4)["아이패드","햇반","생수","피아노"]

택배들.pop();
택배들; // (3)["아이패드","햇반","생수"]
택배들.pop();
택배들; // (2)["아이패드","햇반"]

let 택배 = 택배들.pop(); //"햇반"
택배들; // (1)["아이패드"]
택배; //"햇반"
